        $totalCostAfterVAT = $totalItemCost + $totalTax;

        // Expense Debit
        $expenseAccount = Account::findOrFail(Variable::where('title', 'expense_account')->first()->value);
        $this->createTransaction($receipt->journal_voucher, $expenseAccount->id, $totalCostAfterVAT - $totalTax, 0, $rate, $receipt);

        // Tax Debit
        $taxAccount = Tax::findOrFail($validatedData['tax_id'])->account;
        $this->createTransaction($receipt->journal_voucher, $taxAccount->id, $totalTax, 0, $rate, $receipt);

