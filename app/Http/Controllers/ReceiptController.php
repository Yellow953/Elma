    private function createTransaction($jv, $accountId, $debit, $credit, $rate, $receipt, $supplierId = null)
    {
        Transaction::create([
            'user_id' => auth()->id(),
            'journal_voucher_id' => $jv->id,
            'account_id' => $accountId,
            'supplier_id' => $supplierId,
            'currency_id' => $receipt->currency_id,
            'debit' => $debit,
            'credit' => $credit,
            'balance' => $debit - $credit,
            'foreign_currency_id' => $receipt->foreign_currency_id,
            'rate' => $rate,
            'foreign_debit' => $debit * $rate,
            'foreign_credit' => $credit * $rate,
            'foreign_balance' => ($debit - $credit) * $rate,
        ]);
    }
