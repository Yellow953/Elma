                'quantity' => $quantity,
                'unit_cost' => $unitCost,
                'total_cost' => $totalCost,
                'vat' => $vat,
                'rate' => $rate,
                'total_cost_after_vat' => $totalCost + $vat,
                'total_foreign_cost' => $totalCost * $rate,
            ]);

            $totalItemCost += $totalCost;
            $totalTax += $vat;
